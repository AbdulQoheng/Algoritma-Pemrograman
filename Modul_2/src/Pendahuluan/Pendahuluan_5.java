public class Pendahuluan_5 {

    public static void main(String[] args) {

        int a = 30;
        int b = 20;

        boolean perbandingan = a == b;
        int incremen = a++;
        int decremen = a--;
        int penjumlahan = a + b;
        int pengurangan = a - b;
        int perkalian = a * b;
        int pembagian = a / b;
        int modulus = a % b;
        
        System.out.println("Contoh perbandingan");
        System.out.println(perbandingan);
        System.out.println("Contoh Incremen / Decremen");
        System.out.println(incremen);
        System.out.println(decremen);
        System.out.println("Contoh Aritmatika");
        System.out.println(penjumlahan);
        System.out.println(pengurangan);
        System.out.println(perkalian);
        System.out.println(pembagian);
        System.out.println(modulus);

    }

}
